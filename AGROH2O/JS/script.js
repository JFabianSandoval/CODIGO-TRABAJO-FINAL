function showServicios() {
    var serviciosDiv = document.getElementById("ServiciosDIV");
    if (serviciosDiv.style.display === "none") {
        serviciosDiv.style.display = "flex";
    } else {
        serviciosDiv.style.display = "none";
    }
}
function showContactanos() {
    var contactanosDiv = document.getElementById("ContactanosDIV");
    if (contactanosDiv.classList.contains("Hidden")) {
        contactanosDiv.classList.remove("Hidden");
    } else {
        contactanosDiv.classList.add("Hidden");
    }
}

